    if pacman_obj[0] >= screen.get_width() -  TS: pacman_obj = (screen.get_width()-TS,  pacman_obj[1])
    if pacman_obj[1] >= screen.get_height() - TS: pacman_obj = (pacman_obj[0], screen.get_height()-TS)
